
public class Ngay {
	private int ngay, thang, nam;
	
	public Ngay(int ng, int th, int nm) {
		this.ngay = ng;
		this.thang = th;
		this.nam = nm;
	}

	public int getNgay() {
		return ngay;
	}

	public void setNgay(int ngay) {
		this.ngay = ngay;
	}

	public int getThang() {
		return thang;
	}

	public void setThang(int thang) {
		this.thang = thang;
	}

	public int getNam() {
		return nam;
	}

	public void setNam(int nam) {
		this.nam = nam;
	}
	
	
}
