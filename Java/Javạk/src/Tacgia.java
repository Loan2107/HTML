
public class Tacgia {
	private String tentg;
	private Ngay d;
	public Tacgia(String tentg, Ngay d) {
		this.tentg = tentg;
		this.d = d;
	}
	public String getTentg() {
		return tentg;
	}
	public void setTentg(String tentg) {
		this.tentg = tentg;
	}
	public Ngay getD() {
		return d;
	}
	public void setD(Ngay d) {
		this.d = d;
	}
	
}
