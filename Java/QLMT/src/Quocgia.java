
public class Quocgia {
	private String Ma,TenQG;

	public Quocgia(String ma, String tenQG) {
		Ma = ma;
		TenQG = tenQG;
	}

	public String getMa() {
		return Ma;
	}

	public void setMa(String ma) {
		Ma = ma;
	}

	public String getTenQG() {
		return TenQG;
	}

	public void setTenQG(String tenQG) {
		TenQG = tenQG;
	}
	
	
}
